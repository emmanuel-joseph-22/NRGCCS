        <p>The LCD monitor is thinner (thin[n] + er) and newer (new + er and not new[w] + er) compared to CRT monitor.</p>
        <p>We write the consonant again if an adjective ends in one vowel and one consonant before adding er. But never for a word ending in w.</p>
